// localStorage functionality
const STORAGE_KEY = 'nathan-photos';

const savePhotos = (photosArray) => {
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(photosArray));
  } catch (error) {
    console.error('Error saving photos to localStorage:', error);
  }
};

const loadPhotos = () => {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored) {
      const parsed = JSON.parse(stored);
      // Check if the stored data is valid (has the right structure)
      if (Array.isArray(parsed) && parsed.length > 0 && parsed[0].src) {
        return parsed;
      }
    }
    return initialPhotos;
  } catch (error) {
    console.error('Error loading photos from localStorage:', error);
    return initialPhotos;
  }
};

export const refreshPhotos = () => {
  return loadPhotos();
};

// CRUD operations
export const addPhoto = (newPhoto) => {
  const photos = loadPhotos();
  const photoToAdd = {
    id: Date.now(),
    timestamp: new Date().toLocaleDateString(),
    ...newPhoto
  };
  photos.push(photoToAdd);
  savePhotos(photos);
  return photos;
};

export const updatePhoto = (id, updatedPhoto) => {
  const photos = loadPhotos();
  const index = photos.findIndex(photo => photo.id === id);
  if (index !== -1) {
    photos[index] = { ...photos[index], ...updatedPhoto };
    savePhotos(photos);
  }
  return photos;
};

export const deletePhoto = (id) => {
  const photos = loadPhotos();
  const filteredPhotos = photos.filter(photo => photo.id !== id);
  savePhotos(filteredPhotos);
  return filteredPhotos;
};


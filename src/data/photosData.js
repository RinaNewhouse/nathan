// Get stats
export const getPhotosStats = () => {
  const categories = new Set();
  
  initialPhotos.forEach(photo => {
    if (photo.category) {
      // Handle multiple categories separated by spaces
      photo.category.split(' ').forEach(cat => {
        if (cat.trim()) categories.add(cat.trim());
      });
    }
  });
  
  return {
    totalPhotos: initialPhotos.length,
    categories: categories.size
  };
}; 